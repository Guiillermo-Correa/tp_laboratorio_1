#ifndef FUNCIONES_H_
#define FUNCIONES_H_

/** \brief Sumo los numeros  decimales ingresados
 *
 * \param Primer numero ingresado (numeroUno)
 * \param Segundo numero ingresado (numeroDos)
 * \return Retrona el resultado de la suma del primer numero ingresado y el segundo
 *
 */

float suma(float numeroUno, float numeroDos);

/** \brief Resto los numero decimales ingresados
 *
 * \param Primer numero ingresado (numeroUno)
 * \param Segundo numero ingresado (numeroDos)
 * \return  Retorna la resta del primer numero y del segundo
 *
 */

float resta(float numeroUno, float numeroDos);

/** \brief Multiplica los numeros decimales ingresados
 *
 * \param Primer numero ingresado (numeroUno)
 * \param Segundo numero ingresado (numeroDos)
 * \return Retorna la multipicacion del primer numero y el segundo
 *
 */

float multiplicacion(float numeroUno ,float numeroDos);
/** \brief Divido los numeros decimales ingresados
 *
 * \param Ingreso el primer numero (numeroUno)
 * \param Ingreso el segundo numero (numeroDos)
 * \return Retorno la multiplicaion del primer y del segundo numero
 *
 */

float division(float numeroUno, float numeroDos);
/** \brief Factorea el numero decimal ingresado
 *
 * \param Ingreso el numero a factorear (numeroUno)
 * \return Retorna el resultado del numero factoreado
 *
 */

float factorial(float numeroUno);
/** \brief Factorea el numero decimal ingresado
 *
 * \param Ingreso el numero a factorear (numeroDos)
 * \return Retorna el resultado del numero factoreado
 *
 */

float fact(float numeroDos);


#endif /* FUNCIONES_H_ */
